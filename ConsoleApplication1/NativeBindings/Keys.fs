module NativeBindings.Keys

// Taken from https://docs.microsoft.com/en-us/windows/win32/inputdev/virtual-key-codes
let convertStringKeyToCode (keyString: string) =
    match keyString with
    | "VK_LBUTTON" -> Some 0x01
    | "VK_RBUTTON" -> Some 0x02
    | "VK_CANCEL" -> Some 0x03
    | "VK_MBUTTON" -> Some 0x04
    | "VK_XBUTTON1" -> Some 0x05
    | "VK_XBUTTON2" -> Some 0x06
    | "VK_BACK" -> Some 0x08
    | "VK_TAB" -> Some 0x09
    | "VK_CLEAR" -> Some 0x0C
    | "VK_RETURN" -> Some 0x0D
    | "VK_SHIFT" -> Some 0x10
    | "VK_CONTROL" -> Some 0x11
    | "VK_MENU" -> Some 0x12
    | "VK_PAUSE" -> Some 0x13
    | "VK_CAPITAL" -> Some 0x14
    | "VK_KANA" -> Some 0x15
    | "VK_HANGUEL" -> Some 0x15
    | "VK_HANGUL" -> Some 0x15
    | "VK_JUNJA" -> Some 0x17
    | "VK_FINAL" -> Some 0x18
    | "VK_HANJA" -> Some 0x19
    | "VK_KANJI" -> Some 0x19
    | "VK_ESCAPE" -> Some 0x1B
    | "VK_CONVERT" -> Some 0x1C
    | "VK_NONCONVERT" -> Some 0x1D
    | "VK_ACCEPT" -> Some 0x1E
    | "VK_MODECHANGE" -> Some 0x1F
    | "VK_SPACE" -> Some 0x20
    | "VK_PRIOR" -> Some 0x21
    | "VK_NEXT" -> Some 0x22
    | "VK_END" -> Some 0x23
    | "VK_HOME" -> Some 0x24
    | "VK_LEFT" -> Some 0x25
    | "VK_UP" -> Some 0x26
    | "VK_RIGHT" -> Some 0x27
    | "VK_DOWN" -> Some 0x28
    | "VK_SELECT" -> Some 0x29
    | "VK_PRINT" -> Some 0x2A
    | "VK_EXECUTE" -> Some 0x2B
    | "VK_SNAPSHOT" -> Some 0x2C
    | "VK_INSERT" -> Some 0x2D
    | "VK_DELETE" -> Some 0x2E
    | "VK_HELP" -> Some 0x2F
    | "0" -> Some 0x30
    | "1" -> Some 0x31
    | "2" -> Some 0x32
    | "3" -> Some 0x33
    | "4" -> Some 0x34
    | "5" -> Some 0x35
    | "6" -> Some 0x36
    | "7" -> Some 0x37
    | "8" -> Some 0x38
    | "9" -> Some 0x39
