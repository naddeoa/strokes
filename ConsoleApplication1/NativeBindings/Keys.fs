    | "VK_NUMPAD7" -> Some 0x67
    | "VK_NUMPAD8" -> Some 0x68
    | "VK_NUMPAD9" -> Some 0x69
    | "VK_MULTIPLY" -> Some 0x6A
    | "VK_ADD" -> Some 0x6B
    | "VK_SEPARATOR" -> Some 0x6C
    | "VK_SUBTRACT" -> Some 0x6D
