    | "b" -> Some 0x42
    | "c" -> Some 0x43
    | "d" -> Some 0x44
    | "e" -> Some 0x45
    | "f" -> Some 0x46
    | "g" -> Some 0x47
    | "h" -> Some 0x48
    | "i" -> Some 0x49
    | "j" -> Some 0x4A
    | "k" -> Some 0x4B
    | "l" -> Some 0x4C
    | "m" -> Some 0x4D
    | "n" -> Some 0x4E
    | "o" -> Some 0x4F
    | "p" -> Some 0x50
    | "q" -> Some 0x51
    | "r" -> Some 0x52
    | "s" -> Some 0x53
    | "t" -> Some 0x54
    | "u" -> Some 0x55
    | "v" -> Some 0x56
    | "w" -> Some 0x57
    | "x" -> Some 0x58
    | "y" -> Some 0x59
    | "z" -> Some 0x5A
